//Infinite for Loop
public class Lll_infinite_for_loop {
	public static void main(String[] args) {
		//Java Infinite for Loop
				/*If we set the test expression in such a way that it never evaluates to false,
				the for loop will run forever. This is called infinite for loop. For example,*/
				// Infinite for Loop
				int sum = 0;

		        for (int i = 1; i <= 10; --i) {
		            System.out.println("Hello");
		        }
		        /*Here, the test expression ,i <= 10, is never false and Hello is printed repeatedly
		         *  until the memory runs out.*/
	}
}
