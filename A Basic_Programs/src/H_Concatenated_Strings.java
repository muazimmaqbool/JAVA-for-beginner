//Concatenated strings : means merging two strings
// Here, we have used the + operator to concatenate (join) the two strings: "I am " and "awesome.".
public class H_Concatenated_Strings {
	public static void main(String[] args) {
		String str1="I am";
		String str2=" Awesom";
		System.out.println(str1+str2);
		String str3=" Enroll =";
		int enroll=6655;
		System.out.println(str3+" "+enroll);
		System.out.println("Asalamualikum" +" Muazim Maqbool");
		String str=str1+str2+str3;
		System.out.println(str);

	}

}
